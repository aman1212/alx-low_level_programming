/**
 *_islower - checks for lowercase characters
 *
 *Description: checks for lowercase
 *
 *Return:  1 - l0wercase 0 - otherwise
 *
 *@c:input
 */
int _islower(int c)
{
	if(isupper(ch))
    {
         return (1);
    }
    else if(islower(ch))
    {
        return (0);
    }
    else
    {
        return (0);
    }
}